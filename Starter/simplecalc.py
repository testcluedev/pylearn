# Create a simple calculator that can perform basic arithmetic operations like addition, subtraction, multiplication, and division.

num1 = int(input("please input first number: "))
num2 = int(input("please input second number: "))

# Add the Numbers:
addnum = num1+num2
print(f"Sum of numbers is {addnum}")

# Subtract the Numbers:
subnum = num1-num2
print(f"Substraction of the numbers is {subnum}")

# Subtract the Numbers:
mulnum = num1*num2
print(f"Multiplication of the numbers is {mulnum}")

# Divide the Numbers:
divnum = num1/num2
print(f"Division of the numbers is {divnum}")

